package domain

import (
	"sync"
	"sync/atomic"
)

// MemberID — уникальный идентификатор участника в пределах комнаты.
// Диапазон: [minMemberID maxMemberID] Генерируется атомарным счётчиком.
type MemberID int64

// Member представляет участника комнаты чата.
// Управляет каналом исходящих событий и состоянием подключения.
// Потокобезопасен для чтения через геттеры.
type Member struct {
	// id — уникальный идентификатор участника в комнате.
	// Неизменяем после создания.
	id MemberID

	// pubKeyB64 — base64-представление публичного ключа участника.
	// Используется для быстрого lookup в room.memberByPubKey.
	// Неизменяем после создания.
	pubKeyB64 string

	// events — буферизованный канал исходящих событий для этого участника.
	// Пишется только горутиной Room.Run()
	// Размер буфера: cfg.eventChannelBufSize.
	events chan Event

	overflow *memberOverflowBuf // Буфер "последнего шанса" для медленных клиентов

	// done — сигнал завершения работы участника.
	// Закрывается при отключении (явном или из-за ошибки).
	// Используется для координации завершения горутин.
	done chan struct{}

	// closeOnce гарантирует, что ресурсы будут освобождены только один раз.
	// Защищает от повторного закрытия каналов.
	closeOnce sync.Once

	// activated — флаг, указывающий, был ли участник полностью инициализирован
	// (отправлен bootstrap + снапшот истории + событие joined).
	// Используется для защиты от гонок: POST-запросы до активации отклоняются.
	activated atomic.Bool
}

// NewMember создаёт нового участника с заданными параметрами.
// Канал событий инициализируется с размером буфера из пакетной конфигурации.
// Вызывается только внутри Room.Join() через фабрику
// func NewMember(id MemberID, pubKeyB64 string) *Member { } - нет

// ID возвращает уникальный идентификатор участника.
// Потокобезопасен, может вызываться из любой горутины.
func (m *Member) ID() MemberID {
	return m.id
}

// PubKeyB64 возвращает base64-представление публичного ключа участника.
// Используется для lookup в room.memberByPubKey и для логирования.
// Потокобезопасен, может вызываться из любой горутины.
func (m *Member) PubKeyB64() string {
	return m.pubKeyB64
}

// Events возвращает канал исходящих событий для стрима.
// Пишется только горутиной Room.Run()
// Не закрывайте этот канал вручную — используйте Close().
func (m *Member) Events() <-chan Event {
	return m.events
}

// Done возвращает канал, который закрывается при завершении работы участника.
// Используйте для graceful shutdown в горутине стрима:
//
//	select {
//	case <-member.Done():
//	    return // участник отключился
//	case evt := <-member.Events():
//	    // обработка события
//	}
func (m *Member) Done() <-chan struct{} {
	return m.done
}

// Close корректно завершает работу участника:
// - Закрывает канал done для сигнализации завершения
// - Закрывает канал events для остановки чтения
// - Гарантирует идемпотентность через sync.Once
// Вызывайте при отключении участника (явно или из-за ошибки).
func (m *Member) Close() {
	m.closeOnce.Do(func() {
		close(m.done)
		close(m.events)
	})
}

// SetActivated помечает участника как полностью инициализированного.
// Вызывается после успешной отправки bootstrap + снапшота истории + события joined.
// Позволяет отклонять POST-запросы, пришедшие до завершения инициализации.
func (m *Member) SetActivated() {
	m.activated.Store(true)
}

// IsActivated проверяет, завершена ли инициализация участника
func (m *Member) IsActivated() bool {
	return m.activated.Load()
}
