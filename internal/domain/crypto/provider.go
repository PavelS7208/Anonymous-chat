package crypto

// Provider абстрагирует криптографические операции для доменного слоя.
// Позволяет подменять реализацию (ed25519, mock для тестов) без изменения бизнес-логики.
//
// Контракт:
// - Все методы потокобезопасны (или вызываются из однопоточного контекста сервиса).
// - Ошибки возвращаются через error, а не паники.
// - Ключи передаются как []byte в сыром виде (без base64).
type Provider interface {
	// GenerateKeyPair генерирует новую пару ключей Ed25519 согласно RFC 8032.
	// Возвращает:
	//   - privSeed: 32 байта приватного seed (первые 32 байта приватного ключа)
	//     Отправляется клиенту один раз при инициализации и НЕ хранится на сервере.
	//   - pubKey: 32 байта публичного ключа
	//     Хранится на сервере для верификации подписей и lookup участников.
	//   - error: при ошибке генерации (например, нехватка энтропии)
	GenerateKeyPair() (privSeed, pubKey []byte, err error)

	// Verify проверяет подпись Ed25519 сообщения.
	// Параметры:
	//   - pubKey: сырой публичный ключ (32 байта)
	//   - message: тело сообщения (сырые байты, как получено от клиента)
	//   - signature: сырая подпись (64 байта)
	// Возвращает true, если подпись валидна и соответствует сообщению и ключу.
	//
	// Важно: функция не проверяет формат ключей/подписи — это делается на уровне
	// парсинга (domain/message). Здесь только криптографическая верификация.
	Verify(pubKey, message, signature []byte) bool
}
